    //method for selection sort
    public static void selectionSort(int[]price){
        int size= price.length;
        for(int x=0;x<(size);x++){
            int min_val=x;
            for (int y=x+1;y<size;y++){
                if (price[y]<price[min_val]){
                    min_val=y;
                }
            }
            //swapping the minimum value to the lowest index in each loop
            int swap=price[min_val];
            price[min_val]=price[x];
            price[x]=swap; 
            System.out.println(price[x]);
        }
    }
    //method for binary search
    public int binarySearch(int[]price, int value1 ){
        int l=0, r=price.length-1;
        while (l<=r){
            int m =(l+r)/2;
            if (price[m]==value1){
                return m;
            }
            if (price[m] < value1){
                l=m+1; 
            }
            if (price[m]> value1){
                r=m-1;
            }
        }
        return -1;
    }
    

