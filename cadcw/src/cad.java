        int i=jTable1.getRowCount();    
        int j=jTable1.getColumnCount();
        String val = (String) jComboBox2.getSelectedItem();
        int index = jComboBox2.getSelectedIndex();
        //removing the rows from the search table
        int rowCount1 = model.getRowCount();
