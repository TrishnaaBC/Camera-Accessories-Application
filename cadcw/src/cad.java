        for(int nextRow=0;nextRow<=(i-1);nextRow++){
            String a= jTable1.getValueAt(nextRow,5).toString();
            int value= Integer.parseInt(a);
            price[nextRow]=value;   
        }
        
        if (index !=0){
            selectionSort(price);
            int value1=Integer.parseInt(val);
            int search = binarySearch(price,value1);
            if (search == -1){
                System.out.println ("Element is not present.");
            }
            else {
                String val1=String.valueOf(price[search]);
                String searchArr[]=new String [10];
                int rowNext=0;
                int rowSearch=0;
                boolean checkEmptyPrice=false;
                System.out.println(price[search]);
                if (i!=0){
                    do {
                        if (jTable1.getValueAt(rowNext,5)!=null){
                            String v=jTable1.getValueAt(rowNext,5).toString();
                            if (val1.equals(v)){
                                for(int x=0; x<j;x++){
                                    searchArr[x]=jTable1.getValueAt(rowNext,x).toString();
                                }
                                //adding new row to the search table
                                model.addRow(new Object[]{});
                                for (int y=0;y<j;y++){
                                    jTable3.setValueAt(searchArr[y],rowSearch,y);
                                }
                                rowSearch++;
                            }
                            rowNext++;
                        } else {checkEmptyPrice=true;}
                    }while (rowNext<i && !checkEmptyPrice);
                }
            }
        }else {JOptionPane.showMessageDialog(this, "Please select a price to search.");}      
               
        
